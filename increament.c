/* find increament and decreament program in c language */
#include <stdio.h>
#include <conio.h>

void main()
{
    int num1,num2,num3,num4;
    num1=20;
    num2=30;
    /* increament program */
    num3=++num1;
    printf("the increament number is %d",num3);

    /* program for decreament */
    num4=--num2;

    printf("the decreament number is %d",num4);
    getch();

}






















